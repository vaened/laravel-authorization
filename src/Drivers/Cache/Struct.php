namespace Vaened\Authorization\Drivers\Cache;

class Struct
{
    private $name;

    public function __construct(string $name)
    {
        $this->name = $name;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public static function make(string $name): Struct
    {
        return new static($name);
    }
}
